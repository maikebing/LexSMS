        /// <summary>
        /// 根据 MCC 获取国家信息
        /// </summary>
        public static string GetCountryName(int mcc)
        {
            return Countries.TryGetValue(mcc, out var country) ? country : "未知";
        }

        /// <summary>
        /// 根据 MCC 和 MNC 获取运营商信息
        /// </summary>
        public static string GetOperatorName(int mcc, int mnc)
        {
            var key = $"{mcc:000}{mnc:00}";
            if (Operators.TryGetValue(key, out var operatorName))
            {
                return operatorName;
            }

            // 如果找不到精确匹配，尝试只用 MCC
            key = $"{mcc:000}{mnc:000}";
            return Operators.TryGetValue(key, out operatorName) ? operatorName : "未知运营商";
        }

        /// <summary>
        /// 获取完整的基站信息描述
        /// </summary>
        public static string GetCellInfoDescription(int mcc, int mnc, int lac, int cellId)
        {
            var country = GetCountryName(mcc);
            var operatorName = GetOperatorName(mcc, mnc);
            
            return $"{country} - {operatorName}";
        }

